namespace TesztIsmetles
{
    public class Alapmuveletek
    {
        public double Osszead(double a, double b)
        {
            return a + b;
        }

        public double Kivon(double a, double b) {
            return a - b;
        }

        public double Szoroz(double a, double b) {
            return a * b;
        }

        public double Oszt(double a, double b) {
            return a / b;
        }

    }
}
